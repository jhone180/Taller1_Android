package com.example.myapplication;

public class Calculadora {

    int sum;
    int multip;

    public int suma(int num1, int num2){
        sum = num1 + num2;
        return sum;
    }

    public int multiplicacion(int num1, int num2){
        multip = num1 * num2;
        return multip;
    }

}
